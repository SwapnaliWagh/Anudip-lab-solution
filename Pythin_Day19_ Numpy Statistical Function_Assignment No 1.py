#Q.11.Compute the median of the flattened NumPy array Input: x_odd = np.array([1, 2, 3, 4, 5, 6, 7])

import numpy as np  #Import the NumPy library

#Input array
x_odd = np.array([1, 2, 3, 4, 5, 6, 7])

#Compute the median of the array
median_value = np.median(x_odd)

#Output the result
print("Median of the given array is :", median_value)

